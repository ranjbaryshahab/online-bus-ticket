function setTicket(id, customerName, gender, origin, destination, departureDate, departureTime, travelId, customerId) {
    document.getElementById("ticketId").value = id;
    document.getElementById("customerName").value = customerName;
    document.getElementById("gender").value = gender;
    document.getElementById("origin").value = origin;
    document.getElementById("destination").value = destination;
    document.getElementById("departureDate").value = departureDate;
    document.getElementById("departureTime").value = departureTime;
    document.getElementById("travelId").value = travelId;
    document.getElementById("customerId").value = customerId;
}
